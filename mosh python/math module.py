import math

val = 3

#square root
sqrtVal = math.sqrt(val)
print(sqrtVal)
print(sqrtVal**(2))

#e^(val)
expVal = math.exp(val)
print(expVal)

#factorials 5! = 5*4*3*2*1
factVal = math.factorial(math.floor(val))
print(factVal)

sinVal = math.sin(val)
print(sinVal)

#round up
ceilingVal = math.ceil(val)
print(ceilingVal)

#round down
floorVal = math.floor(val)
print(floorVal)

